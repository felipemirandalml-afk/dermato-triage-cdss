/**
 * Mapea los datos del formulario al vector de features esperado por el modelo.
 * Implementa una búsqueda dinámica para evitar hardcodear cada feature.
 */
/**
 * Aplica el escalado de StandardScaler usado en el entrenamiento.
 */
function applyScaling(vector) {
  const { mean, scale } = MODEL_DATA.scaling;
  return vector.map((val, i) => (val - mean[i]) / scale[i]);
}

/**
 * Implementación de Softmax para estabilidad numérica.
 */
function softmax(logits) {
  const maxLogit = Math.max(...logits);
  const scores = logits.map(l => Math.exp(l - maxLogit));
  const sumScores = scores.reduce((a, b) => a + b, 0);
  return scores.map(s => s / sumScores);
}
const CONFIDENCE_THRESHOLD = 0.40;

/**
 * Calcula la importancia de las features para una decisión específica.
 */
function calculateFeatureImportance(scaledVector, winningClassIdx) {
    const { features } = MODEL_DATA.metadata;
    const { coefficients } = MODEL_DATA.parameters;
    const weights = coefficients[winningClassIdx];

    const importance = features.map((featKey, i) => {
        const val = scaledVector[i];
        const weight = weights[i];
        const impact = val * weight;
        return {
            key: featKey,
            impact: impact
        };
    }).filter(f => Math.abs(f.impact) > 0.1); 

    return {
        positive: importance.filter(f => f.impact > 0).sort((a, b) => b.impact - a.impact).slice(0, 3),
        negative: importance.filter(f => f.impact < 0).sort((a, b) => a.impact - b.impact).slice(0, 3)
    };
}

/**
 * Ejecuta la predicción del modelo probabilístico.
 * @param {Array} X - Vector de features unificado (raw)
 */
export function predictProbabilisticSyndrome(X) {
  // El modelo espera exactamente las primeras 48 features en el orden definido en constants.js
  const rawVector = X.slice(0, 48);
  const scaledVector = applyScaling(rawVector);
  
  const { intercept, coefficients } = MODEL_DATA.parameters;
  const { classes } = MODEL_DATA.metadata;

  // Calcular Logits por cada clase
  const logits = intercept.map((b0, classIdx) => {
    const classWeights = coefficients[classIdx];
    return b0 + scaledVector.reduce((sum, val, featIdx) => sum + (val * classWeights[featIdx]), 0);
  });

  const probabilities = softmax(logits);
  
  // Mapear probabilidades a clases
  const syndrome_probabilities = {};
  let top_syndrome = classes[0];
  let top_probability = 0;
  let top_class_idx = 0;

  probabilities.forEach((prob, i) => {
    const className = classes[i];
    syndrome_probabilities[className] = prob;
    if (prob > top_probability) {
      top_probability = prob;
      top_syndrome = className;
      top_class_idx = i;
    }
  });

  // Calibración de Confianza
  const isConfident = top_probability >= CONFIDENCE_THRESHOLD;
  const confidence_level = top_probability > 0.75 ? "high" : (isConfident ? "medium" : "low");

  // Calcular Importancia de Features (Explainability)
  const feature_importance = calculateFeatureImportance(scaledVector, top_class_idx);

  return {
    top_syndrome: isConfident ? top_syndrome : null,
    top_probability,
    confidence_level,
    feature_importance,
    message: isConfident ? null : "Patrón indeterminado o mixto - se sugiere evaluación clínica directa",
    syndrome_probabilities
  };
}
